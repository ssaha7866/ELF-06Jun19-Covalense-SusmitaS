/*
 * WAP to store five student bean objects into the arraylist and
 *  display the contents of those student objects 
 */
package com.covalense.java.fifthassignment;

public class StudentBeanClass {
	private String name;
	private int id;
	private double marks;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public double getMarks() {
		return marks;
	}

	public void setMarks(double marks) {
		this.marks = marks;
	}

}
