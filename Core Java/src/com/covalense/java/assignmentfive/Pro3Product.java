package com.covalense.java.assignmentfive;

/**
 * WAP to store five product bean objects into Array list and
 *  display only the products which has a cost price less than 2000
 */

public class Pro3Product {
	private String name;
	private int cost;
	private double rating;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getCost() {
		return cost;
	}

	public void setCost(int cost) {
		this.cost = cost;
	}

	public double getRating() {
		return rating;
	}

	public void setRating(double rating) {
		this.rating = rating;
	}

}
