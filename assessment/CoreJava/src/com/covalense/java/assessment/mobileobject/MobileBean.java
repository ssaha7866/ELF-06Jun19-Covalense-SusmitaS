package com.covalense.java.assessment.mobileobject;
/**
 * 
 * Mobile bean class containing all its behaviors
 *
 */
public class MobileBean {
	
	private String name;
	private int cost;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCost() {
		return cost;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}

}
